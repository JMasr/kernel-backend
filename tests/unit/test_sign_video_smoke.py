async def test_wid_derivation_deterministic(synthetic_video: Path) -> None:
    """WID is deterministically derived from signature and content_id."""
    private_pem, public_pem = generate_keypair()
    cert = _make_cert(public_pem)

    result = await sign_video(
        media_path=synthetic_video,
        certificate=cert,
        private_key_pem=private_pem,
        storage=FakeStorage(),
        registry=FakeRegistry(),
        pepper=PEPPER,
        media=MediaService(),
    )

    re_derived = derive_wid(result.signature, result.content_id)
    assert result.wid == re_derived
